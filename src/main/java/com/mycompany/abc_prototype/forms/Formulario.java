    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int indice = this.listClientes.getSelectedIndex();
        lista.remove(indice);
        actualizarLista();
        JOptionPane.showMessageDialog(rootPane, "Se elimin√≥ correctamente");
    }//GEN-LAST:event_btnEliminarActionPerformed

     private List<Cliente> lista = new ArrayList<Cliente>();
    
    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        Cliente client = new Cliente();
        client.setNombre(this.txtNombre.getText());
        client.setApellido(this.txtApellido.getText());
        client.setEmail(this.txtEmail.getText());
        client.setTelefono(this.txtTelefono.getText());

        lista.add(client);
        actualizarLista();
        JOptionPane.showMessageDialog(rootPane, "Se agrego correctamente");
        
        limpiarCajasDeTexto();
    }//GEN-LAST:event_CalcularActionPerformed
    private void limpiarCajasDeTexto(){
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtEmail.setText("");
        this.txtTelefono.setText("");
        
    }
